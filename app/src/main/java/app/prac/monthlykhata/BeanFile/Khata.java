package app.prac.monthlykhata.BeanFile;

public class Khata {
    private String khata_date;
    private String name;
    private String id;

    public void setkhataDate(String khata_date) {
        this.khata_date = khata_date;
    }

    public String getkhataDate() {
        return khata_date;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }
}
